# Check for remaining null values in the merged dataset
print("\n--- Missing values in Merged Dataset ---")
print(merged_df.isnull().sum()[merged_df.isnull().sum() > 0])

# For simplicity, we'll fill any remaining numerical NaN values with the median of their respective columns.
# This is a common strategy when the proportion of missing values is not too high and we want to retain data.
for col in merged_df.select_dtypes(include=np.number).columns:
    if merged_df[col].isnull().any():
        median_val = merged_df[col].median()
        merged_df[col].fillna(median_val, inplace=True)

print("\n--- Missing values after final imputation ---")
print(merged_df.isnull().sum()[merged_df.isnull().sum() > 0])

