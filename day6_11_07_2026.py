columns_to_drop = ['INDICATOR', 'SUBJECT', 'MEASURE', 'FREQUENCY', 'ObjectId', 'CTS_Code', 'CTS_Name', 'CTS_Full_Descriptor', 'Source', 'Unit']
# Drop columns that are metadata or not relevant for direct modeling
merged_df.drop(columns=columns_to_drop, inplace=True, errors='ignore')

